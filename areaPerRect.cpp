// This program calculates Area and Perimeter of a rectangle

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions:" << std::endl;
    std::cout << "7cm x 4cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "Area is: " <<(7*4) << "cm^2" << std::endl;
    std::cout << "Perimeter is: " << (2*(7+4)) << "cm" << std::endl;
}
